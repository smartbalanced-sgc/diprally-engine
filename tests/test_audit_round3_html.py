def test_text_and_html_agree_on_chip_count():
    """The text-report _reliability_warnings_line and HTML
    _reliability_chips_html must surface the same flags — they share
    _reliability_chips. Regression guard against divergence."""
    from src.reporter import _reliability_warnings_line

    base = [_make_signal("HIGH")] * 3 + [_make_signal("LOW")] * 9
    vol = _make_vol_profile(alpha_plus_beta=0.9999, divergence=19.3)

    chips = _reliability_chips(vol_profile=vol, base_signals=base,
                                 sigma_class_mismatch="x")
    text_line = _reliability_warnings_line(vol_profile=vol, base_signals=base,
                                             sigma_class_mismatch="x")
    html = _reliability_chips_html(vol_profile=vol, base_signals=base,
                                     sigma_class_mismatch="x")

    # Both renderers reflect every chip
    for label, _ in chips:
        assert label in text_line, f"text missing chip: {label}"
        assert label in html, f"html missing chip: {label}"


