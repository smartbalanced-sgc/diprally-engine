"""Tests for the 2026-05-24 audit round 3 fix: surface reliability
chips + Pass 2 verdicts + reasoning in the HTML dashboard.

Round 2 (PR #67) added these to the TEXT report (format_report). The
HTML dashboard is generated by a separate code path
(generate_html_dashboard) which was NOT updated — meaning the
operator-facing surface didn't show what the engine knew.

Evidence: SATS dashboard rendered today showed `σ 61%` in the
metadata but no near-IGARCH warning, no σ-divergence warning, no
weak-signals warning — even though the engine's text report had
all three.
"""
from __future__ import annotations

import sys
from pathlib import Path
from types import SimpleNamespace

import pytest

_REPO_ROOT = Path(__file__).resolve().parent.parent
if str(_REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(_REPO_ROOT))

from src.reporter import _reliability_chips, _reliability_chips_html


def _make_vol_profile(alpha_plus_beta=0.50, divergence=5.0):
    return SimpleNamespace(
        garch_alpha_plus_beta=alpha_plus_beta,
        triangulation={"divergence": divergence, "blended": 0.50, "anchors": {}},
    )


def _make_signal(confidence="HIGH", is_absent=False):
    return SimpleNamespace(confidence=confidence, is_absent=is_absent)


# =============================================================================
# _reliability_chips — shared helper used by both text + HTML renderers
# =============================================================================

def test_chips_returns_label_and_severity_tuples():
    chips = _reliability_chips(
        vol_profile=_make_vol_profile(alpha_plus_beta=0.9999, divergence=20.0),
        base_signals=[_make_signal("HIGH")] * 12,
        sigma_class_mismatch="auto MID, hint HIGH",
    )
    # near-IGARCH (red), σ divergence (orange), σ-class mismatch (yellow)
    assert len(chips) == 3
    severities = [sev for _, sev in chips]
    assert "red" in severities
    assert "orange" in severities
    assert "yellow" in severities


def test_chips_empty_on_clean_run():
    chips = _reliability_chips(
        vol_profile=_make_vol_profile(),
        base_signals=[_make_signal("HIGH")] * 12,
        sigma_class_mismatch=None,
    )
    assert chips == []


